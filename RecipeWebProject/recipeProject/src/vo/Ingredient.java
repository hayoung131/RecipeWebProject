package vo;

public class Ingredient {
	
	private int ingredient_id;
	private int num;
	private String searching_ingredient;
	private String amount;
	private String measu;
	
	public int getIngredient_id() {
		return ingredient_id;
	}
	public void setIngredient_id(int ingredient_id) {
		this.ingredient_id = ingredient_id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getSearching_ingredient() {
		return searching_ingredient;
	}
	public void setSearching_ingredient(String searching_ingredient) {
		this.searching_ingredient = searching_ingredient;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getMeasu() {
		return measu;
	}
	public void setMeasu(String measu) {
		this.measu = measu;
	}
	
	
	
}
